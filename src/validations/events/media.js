/**
 * Media Events
 * 
 * Sr.  Event       	    Description
 * 1.   onabort     	    The event occurs when the loading of a media is aborted
 * 2.   oncanplay   	    The event occurs when the browser can start playing the media (when it has buffered enough to begin)
 * 3.   oncanplaythrough    The event occurs when the browser can play through the media without stopping for buffering	
 * 4.   ondurationchange	The event occurs when the duration of the media is changed
 * 5.   onemptied       	The event occurs when something bad happens and the media file is suddenly unavailable (like unexpectedly disconnects)
 * 6.   onended             The event occurs when the media has reach the end (useful for messages like "thanks for listening")
 * 7.   onerror         	The event occurs when an error occurred during the loading of a media file
 * 8.   onloadeddata    	The event occurs when media data is loaded
 * 9.   onloadedmetadata   	The event occurs when meta data (like dimensions and duration) are loaded
 * 10.  onloadstart     	The event occurs when the browser starts looking for the specified media
 * 11.  onpause         	The event occurs when the media is paused either by the user or programmatically
 * 12.  onplay          	The event occurs when the media has been started or is no longer paused
 * 13.  onplaying       	The event occurs when the media is playing after having been paused or stopped for buffering
 * 14.  onprogress      	The event occurs when the browser is in the process of getting the media data (downloading the media)
 * 15.  onratechange    	The event occurs when the playing speed of the media is changed
 * 16.  onseeked        	The event occurs when the user is finished moving/skipping to a new position in the media
 * 17.  onseeking       	The event occurs when the user starts moving/skipping to a new position in the media
 * 18.  onstalled       	The event occurs when the browser is trying to get media data, but data is not available
 * 19.  onsuspend       	The event occurs when the browser is intentionally not getting media data
 * 20.  ontimeupdate    	The event occurs when the playing position has changed (like when the user fast forwards to a different point in the media)
 * 21.  onvolumechange  	The event occurs when the volume of the media has changed (includes setting the volume to "mute")
 * 22.  onwaiting       	The event occurs when the media has paused but is expected to resume (like when the media pauses to buffer more data)	
 */