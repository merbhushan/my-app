/**
 * Mouse Events
 * 
 * Sr.      Event       	Description	DOM
 * 1.       onclick     	The event occurs when the user clicks on an element
 * 2.       oncontextmenu  	The event occurs when the user right-clicks on an element to open a context menu
 * 3.       ondblclick  	The event occurs when the user double-clicks on an element
 * 4.       onmousedown 	The event occurs when the user presses a mouse button over an element
 * 5.       onmouseenter	The event occurs when the pointer is moved onto an element
 * 6.       onmouseleave	The event occurs when the pointer is moved out of an element
 * 7.       onmousemove     The event occurs when the pointer is moving while it is over an element
 * 8.       onmouseover 	The event occurs when the pointer is moved onto an element, or onto one of its children
 * 9.       onmouseout  	The event occurs when a user moves the mouse pointer out of an element, or out of one of its children
 * 10.      onmouseup   	The event occurs when a user releases a mouse button over an element	2
 */