/**
 * Drag Events
 * 
 * Sr.  Event	        Description
 * 1.   ondrag      	The event occurs when an element is being dragged
 * 2.   ondragend   	The event occurs when the user has finished dragging an element
 * 3.   ondragenter 	The event occurs when the dragged element enters the drop target
 * 4.   ondragleave 	The event occurs when the dragged element leaves the drop target
 * 5.   ondragover  	The event occurs when the dragged element is over the drop target
 * 6.   ondragstart 	The event occurs when the user starts to drag an element
 * 7.   ondrop      	The event occurs when the dragged element is dropped on the drop target
 */