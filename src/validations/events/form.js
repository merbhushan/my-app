/**
 * Form Events
 * 
 * Sr.  Event	        Description
 * 1.   onblur      	The event occurs when an element loses focus
 * 2.   onchange    	The event occurs when the content of a form element, the selection, or the checked state have changed (for <input>, <select>, and <textarea>)
 * 3.   onfocus     	The event occurs when an element gets focus
 * 4.   onfocusin   	The event occurs when an element is about to get focus
 * 5.   onfocusout  	The event occurs when an element is about to lose focus	
 * 6.   oninput     	The event occurs when an element gets user input
 * 7.   oninvalid   	The event occurs when an element is invalid
 * 8.   onreset     	The event occurs when a form is reset
 * 9.   onsearch    	The event occurs when the user writes something in a search field (for <input="search">)
 * 10.  onselect    	The event occurs after the user selects some text (for <input> and <textarea>)
 * 11.  onsubmit    	The event occurs when a form is submitted	
 */