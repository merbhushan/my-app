/**
 * Frame/Object Events
 * 
 * Sr.  Event       	Description
 * 1.   onabort     	The event occurs when the loading of a resource has been aborted
 * 2.   onbeforeunload 	The event occurs before the document is about to be unloaded
 * 3.   onerror     	The event occurs when an error occurs while loading an external file
 * 4.   onhashchange   	The event occurs when there has been changes to the anchor part of a URL
 * 5.   onload      	The event occurs when an object has loaded	
 * 6.   onpageshow  	The event occurs when the user navigates to a webpage
 * 7.   onpagehide  	The event occurs when the user navigates away from a webpage
 * 8.   onresize    	The event occurs when the document view is resized
 * 9.   onscroll    	The event occurs when an element's scrollbar is being scrolled
 * 10   onunload    	The event occurs once a page has unloaded (for <body>)	
 */